package org.example;

public class Calculator {
    public long add(int a, int b) {
        return (long)a + (long)b;
    }

    public int minus(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public double divide(int a, int b) {
        return (double) a / b;
    }

}
